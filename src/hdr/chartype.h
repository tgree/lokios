#ifndef LOKIOS_CHARTYPE_H
#define LOKIOS_CHARTYPE_H

#define CHECKER(func,vh,vl) \
    static constexpr bool func(char c) \
    { \
        if (c < 0) \
            return false; \
        if (c < 64) \
            return vl & (1ULL << c); \
        return vh & (1ULL << (c - 64)); \
    }

CHECKER(kisalnum, 0x07FFFFFE07FFFFFEULL,0x03FF000000000000ULL);
CHECKER(kisalpha, 0x07FFFFFE07FFFFFEULL,0x0000000000000000ULL);
CHECKER(kiscntrl, 0x8000000000000000ULL,0x00000000FFFFFFFFULL);
CHECKER(kisdigit, 0x0000000000000000ULL,0x03FF000000000000ULL);
CHECKER(kisgraph, 0x7FFFFFFFFFFFFFFFULL,0xFFFFFFFE00000000ULL);
CHECKER(kislower, 0x07FFFFFE00000000ULL,0x0000000000000000ULL);
CHECKER(kisprint, 0x7FFFFFFFFFFFFFFFULL,0xFFFFFFFF00000000ULL);
CHECKER(kispunct, 0x78000001F8000001ULL,0xFC00FFFE00000000ULL);
CHECKER(kisspace, 0x0000000000000000ULL,0x0000000100003E00ULL);
CHECKER(kisupper, 0x0000000007FFFFFEULL,0x0000000000000000ULL);
CHECKER(kisxdigit,0x0000007E0000007EULL,0x03FF000000000000ULL);
CHECKER(kisascii, 0xFFFFFFFFFFFFFFFFULL,0xFFFFFFFFFFFFFFFFULL);
CHECKER(kisblank, 0x0000000000000000ULL,0x0000000100000200ULL);

#endif /* LOKIOS_CHARTYPE_H */
